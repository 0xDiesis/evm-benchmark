#!/bin/bash
# Discover dynamically assigned Kurtosis ports for Berachain devnet.
# Writes .env with RPC/WS URLs for run-bench.sh consumption.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
ENCLAVE="${ENCLAVE:-bera-bench}"
ENV_FILE="${SCRIPT_DIR}/.env"

# Collect EVM RPC endpoints from all validator EL nodes
RPC_URLS=""
WS_URL=""

add_http() {
    local addr="$1"
    # Kurtosis may return raw host:port or http://host:port
    if [[ "$addr" != http://* ]]; then addr="http://${addr}"; fi
    echo "$addr"
}

for i in 0 1 2 3; do
    SERVICE="el-validator-reth-${i}"
    RAW=$(kurtosis port print "$ENCLAVE" "$SERVICE" eth-json-rpc 2>/dev/null) || continue
    RPC=$(add_http "$RAW")
    if [ -n "$RPC_URLS" ]; then RPC_URLS="${RPC_URLS},"; fi
    RPC_URLS="${RPC_URLS}${RPC}"

    # Use first validator for WS
    if [ -z "$WS_URL" ]; then
        RAW_WS=$(kurtosis port print "$ENCLAVE" "$SERVICE" eth-json-rpc-ws 2>/dev/null) || true
        if [ -n "$RAW_WS" ]; then
            # Ensure ws:// prefix
            WS_URL=$(echo "$RAW_WS" | sed 's|^http://|ws://|; /^ws:\/\//!s|^|ws://|')
        fi
    fi
done

# Also grab the full node RPC
RAW_FULL=$(kurtosis port print "$ENCLAVE" el-full-reth-0 eth-json-rpc 2>/dev/null) || true
if [ -n "$RAW_FULL" ]; then
    RPC_URLS="${RPC_URLS},$(add_http "$RAW_FULL")"
fi

if [ -z "$RPC_URLS" ]; then
    echo "ERROR: Could not discover any Berachain RPC endpoints."
    echo "Is the enclave '${ENCLAVE}' running? Check: kurtosis enclave inspect ${ENCLAVE}"
    exit 1
fi

cat > "$ENV_FILE" <<EOF
# Auto-generated by discover-ports.sh — do not edit
BERA_RPC=${RPC_URLS}
BERA_WS=${WS_URL}
BERA_CHAIN_ID=80087
EOF

echo "Berachain RPC endpoints discovered:"
echo "  HTTP: ${RPC_URLS}"
echo "  WS:   ${WS_URL}"
echo "  Written to: ${ENV_FILE}"
